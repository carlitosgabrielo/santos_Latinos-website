document.addEventListener("DOMContentLoaded", () => {

    const infoDiv = document.getElementById("info");
    const estados = document.querySelectorAll("#label_points circle");

    console.log("Estados encontrados:", estados.length); 

    estados.forEach(estado => {
        estado.addEventListener("click", () => {
            const estadoId = estado.id;
            const informati = santos_de_mexico[estadoId];

            console.log("Presiono", estadoId, informati); 

            if (!informati) return;

            if (informati.Contiene.length === 0) {
                infoDiv.innerHTML = `
                    <h2>${informati.Estado}</h2>
                    <p>Ningun Santo nacio aqui.</p>
                `;
                return;
            }

            let html = "<ul>";

            informati.Contiene.forEach(santo => {
                html += `
                    <li>
                        ${
                            santo.Enlace
                            ? `<a href="${santo.Enlace}" target="_blank">${santo.Nombre}</a>`
                            : santo.Nombre
                        }
                    </li>
                `;
            });

            html += "</ul>";

            infoDiv.innerHTML = `
                <h2>${informati.Estado}</h2>
                ${html}
            `;
        });
    });

});
